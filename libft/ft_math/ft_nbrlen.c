#include "libft.h"

int	ft_nbrlen(long nb)
{
	int	size;

	size = 0;
	if (nb == 0)
		return (1);
	if (nb < 0)
		size++;
	while (nb != 0)
	{
		nb = nb / 10;
		size++;
	}
	return (size);
}
